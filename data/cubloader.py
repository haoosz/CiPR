def CUBData_GCD(root, aug=None, num_lab_classes=100):
    image_size = 224
    normalize = transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize((0.485, 0.456, 0.406), (0.229, 0.224, 0.225)),
        ])
    if aug==None:
        transform = transforms.Compose([
            transforms.Resize(int(image_size / 0.875), 3),
            transforms.CenterCrop(image_size),
            normalize,
        ])
    elif aug=='once':
        transform = transforms.Compose([
            transforms.Resize((image_size, image_size), 3),
            transforms.RandomAffine(degrees=(-45, 45),
                                    translate=(0.1, 0.1), shear=(-15, 15), scale=(0.7, 0.875)),
            transforms.ColorJitter(),
            normalize,
        ])

    elif aug=='twice':
        transform1 = transforms.Compose([
            transforms.Resize((image_size, image_size), 3),
            transforms.RandomAffine(degrees=(-45, 45),
                                    translate=(0.1, 0.1), shear=(-15, 15), scale=(0.7, 0.875)),
            transforms.ColorJitter(),
            normalize,
        ])

        transform2 = transforms.Compose([
            transforms.Resize((image_size, image_size), 3),
            transforms.RandomAffine(degrees=(-45, 45),
                                    translate=(0.1, 0.1), shear=(-15, 15), scale=(0.7, 0.875)),
            transforms.ColorJitter(),
            normalize,
        ])

        transform = TransformTwice(transform1, transform2)
    dataset = ImageFolder(root=root, transform=transform, num_lab_classes=num_lab_classes)
    return dataset

